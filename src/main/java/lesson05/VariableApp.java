package lesson05;

public class VariableApp {
    public static void main(String[] args) {
        int a = 6;
        int b = 5;
        int t = a + b;
        int c = a - b;
        int v = a*b;
        int d = a/b;
        a++;
        b--;
        System.out.println(t);
        System.out.println(c);
        System.out.println(v);
        System.out.println(d);

    }
}
